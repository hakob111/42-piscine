#include <unistd.h>

void	print(int n[4])
{
	write (1, &n[0], 1);
	write (1, &n[1], 1);
	write (1, " ", 1);
	write (1, &n[2], 1);
	write (1, &n[3], 1);
	if (!(n[0] == 57 && n[1] == 56))
	{
		write (1, ", ", 2);
	}
}

void	plasing(int m[4])
{
	while (m[0] <= 57 && m[1] <= 57)
	{
		if (m[0] == 57 && m[1] == 57)
			break ;
		print(m);
		m[3]++;
		if (m[3] > 57)
		{
			m[2]++;
			m[3] = 48;
		}
		if (m[2] > 57)
		{
			m[1]++;
			m[2] = m[0];
			m[3] = m[1] + 1;
		}
		if (m[1] > 57)
		{
			m[0]++;
			m[1] = 48;
			m[2] = m[0];
			m[3] = m[1] + 1;
		}
	}
}

void	ft_print_comb2(void)
{
	int	a[4];

	a[0] = 48;
	a[1] = 48;
	a[2] = 48;
	a[3] = 49;
	plasing(a);
}
